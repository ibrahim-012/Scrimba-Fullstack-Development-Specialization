/*
    can destructure props inline in the function parameter list
    instead of accessing each prop via 'props.propName'
    e.g. export default function Contact({img, name, phone, email})

    instructor said destructuring in parameter list removes visual clutter
    but some developers prefer not to destructure so it's easier to see
    which values are coming from props and which are local variables
*/

export default function Contact(props) {
    return (
        <article className="contact-card">
            <img
                src={props.img}
                alt="Photo of Mr. Whiskerson"
            />
            <h3>{props.name}</h3>
            <div className="info-group">
                <img
                    src="./images/phone-icon.png"
                    alt="phone icon"
                />
                <p>{props.phone}</p>
            </div>
            <div className="info-group">
                <img
                    src="./images/mail-icon.png"
                    alt="mail icon"
                />
                <p>{props.email}</p>
            </div>
        </article>
    )
}

